#include "ft_printf.h"

int	nbr_len(long n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = n * -1;
		i++;
	}
	while (n > 0)
	{
		
		n = n / 10;
		i++;
	}
	return (i);
}